	windowReSize();
	//焦点图
	$('.banner').focusMap();
	//主导航
	$('.menu li').hover(function(){
		var menuObj=$(this).find('div.sub');
		var top=menuObj.height()+26;
		
		$(this).find('a.main').addClass('on');
		menuObj.css('top',-top+'px').show();
	},function(){
		$(this).find('a.main').removeClass('on');
		$(this).find('div.sub').hide();
	});
	//图片展示
	$('.photo div.main').hover(function(){
		$(this).find('img').animate({'opacity':0.8},{queue:false,duration:300});
	},function(){
		$(this).find('img').animate({'opacity':1},{queue:false,duration:300});
	});
	//改变尺寸
	$(window).resize(function(){
		windowReSize();
	});
});
